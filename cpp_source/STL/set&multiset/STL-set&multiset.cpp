#include<set>
#include<iostream>
using namespace std;
//set和mutiset区别
void test01()
{
    set<int> s;
    pair<set<int>::iterator, bool> ret=s.insert(10);
    if (ret.second)
    {
        cout<<"第一次插入成功"<<endl;
    }
    else
    {
        cout<<"第一次插入失败"<<endl;
    }
    ret=s.insert(10);
     if (ret.second)
    {
        cout<<"第一次插入成功"<<endl;
    }
    else
    {
        cout<<"第一次插入失败"<<endl;
    }
    multiset<int>ms;
    //允许插入重复值
    ms.insert(10);
    ms.insert(10);
    for (set<int>::iterator it=ms.begin(); it!=ms.end(); it++)
    {
        cout<<*it<<" ";
    }
    cout<<endl;
}
int main()
{
    test01();
    system("pause");
    return 0;
}